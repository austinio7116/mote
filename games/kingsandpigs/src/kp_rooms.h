/*
 * kp_rooms.h — hand-authored Spelunky-style room chunks (12x6 tiles each).
 *
 * Condensed chamber scale (was 16x8) so the castle feels tight and chunky
 * like the Pixel Frog pack's own rooms. Every room is HAND-CRAFTED for
 * playability, then machine-checked: run `python3 assets/check_rooms.py`
 * after editing (format, per-mouth king-movement accessibility, art clearance).
 *
 * Legend:
 *   #  solid castle wall            .  room interior (pink brick bg)
 *   =  thin wooden plank (one-way)  -  thick beam, metal ends (one-way)
 *   S  standable wall shelf (3-wide beam) with loot on top
 *   O  drop hole (floor row only — punched through to the room below)
 *   d  small gem   D  gem cluster   h  big heart
 *   B  crate spot  b  unlit bomb    E  enemy spot   C  cannon + gunner
 *   W  window      F  hanging banner
 *   e  entrance door                x  exit door
 *
 * Movement budget: jump 2 tiles up, clear 2-tile gaps; platforms max 2 above
 * a standing surface (chain higher ones); mouths (rows 2-4, cols 0/11) open;
 * floor row '#'/'O' only; no pit deeper than 2 tiles.
 */
